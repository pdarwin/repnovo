package projetoExtra;

public class AppStore {
	
	private String nome;

	/**
	 * @param nome
	 */
	public AppStore(String nome) {
		this.nome = nome;
	}

	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
